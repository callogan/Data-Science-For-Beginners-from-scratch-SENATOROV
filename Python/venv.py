# 1. Что делает команда python -m venv venv?
#
#     Команда python -m venv venv создаёт в текущем каталоге папку venv, содержащую отдельную копию интерпретатора Python. Это позволяет изолировать зависимости проекта, предотвращая конфликты с глобально установленными библиотеками. После активации этого окружения все устанавливаемые пакеты будут добавляться только в него, не затрагивая основную систему.

# 1.1. Что делает каждая команда в списке ниже?
#
#     *   pip list – отображает список всех установленных в текущем окружении Python библиотек с их версиями;
#
#     *   pip freeze > requirements.txt – сохраняет список установленных библиотек и их версии в файл requirements.txt, что удобно для воспроизведения окружения;
#
#     *   pip install -r requirements.txt – устанавливает все зависимости, указанные в requirements.txt, в текущее окружение.

# 2. Что делает каждая команда в списке ниже?
#
# *   conda env list – выводит список всех сред (environments), созданных через Conda;
#
# *   conda create -n env_name python=3.5 – создаёт новое окружение с именем env_name, устанавливая в него Python версии 3.5;
#
# *   conda env update -n env_name -f file.yml – обновляет окружение env_name в соответствии с зависимостями, указанными в файле file.yml;
#
# *   source activate env_name – активирует окружение env_name, переключая среду на его использование;
#
# *   source deactivate – отключает текущее активное окружение, возвращая систему к стандартной (базовой) среде;
#
# *   conda clean -a – удаляет временные файлы, кешированные пакеты и неиспользуемые данные, освобождая место.

