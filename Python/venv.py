# 7. Что делают эти команды?
#
#     ```bash
#     pip list
#     pip show,
#     conda list
#     ```
#
#     Команды pip list, pip show и conda list предназначены для отображения информации о установленных пакетах:
#
#     *   pip list — выводит список всех установленных пакетов Python в текущем виртуальном окружении, а также их версии;
#     *   pip show — выводит подробную информацию о конкретном пакете, включая его версию, местоположение, зависимости и другие метаданные;
#     *   conda list — отображает список всех установленных пакетов в текущем окружении conda, включая их версии и дополнительные данные.

# 8. Где по умолчанию больше пакетов venv/pip или conda? и почему дата сайнинисты используют conda?
#
#     По умолчанию в pip/venv больше пакетов, чем в conda.
#
#     Дата-сайентисты часто выбирают conda из-за её удобства, производительности и богатого набора пакетов, специально подобранных для работы с большими данными. Эти пакеты часто скомпилированы для различных операционных системах, что делает работу с ними более эффективной и стабильной.

